package com.example.assingment3;

public class Skifield {
    private String name;
    private int logoId;

    public Skifield(String name, int logoId) {
        this.name = name;
        this.logoId = logoId;
    }

    public String getName() {
        return name;
    }

    public int getLogoId() {
        return logoId;
    }
}
